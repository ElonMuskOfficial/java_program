//6. WAP to display sum of two integers no using static function.

import java.util.*;

class p6
{
	static int n1=10;
	static int n2=20;
	
	static void dis()
	{
		int sum=n1+n2;
		System.out.println("Sum of 2 integers: "+sum);
	}
	
	public static void main(String args[])
	{
		p6.dis();
		
	}
	
}